package JavaBasics;

public class VowelOrConsonant 
{
	public static void main(String[] args) 
	{
		char s= 's';
	    if(s=='A' | s=='E' | s=='I' | s=='O' | s=='U' | s=='a' | s=='e' | s=='i' | s=='o' | s=='u')
	    {
	    	System.out.println(s+" is a vowel");
	    }
	    else
	    {
	    	System.out.println(s+ " is a consonant");
	    }
	}
}

